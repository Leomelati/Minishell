#include "minishell.h"

int	end_var_pos(char *str)
{
	int	i;

	i = 1;
	while (str && str[i])
	{
		if (str[i] == '\"' | str[i] == '\'' | str[i] == '$' | str[i] == ' ')
		{
			return (i - 1);
		}
		i++;
	}
	return (i - 1);
}
